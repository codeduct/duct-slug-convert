const slug = (e) => {
    const characters = {
        'ı': 'i',
        'İ': 'i',
        'ş': 's',
        'Ş': 's',
        'ğ': 'g',
        'Ğ': 'g',
        'ü': 'u',
        'Ü': 'u',
        'ö': 'o',
        'Ö': 'o',
        'ç': 'c',
        'Ç': 'c',
        ' ': '-',
        '_': '-',
        ',': '',
        '.': '',
        ';': '',
        ':': '',
        '!': '',
        '?': '',
        '"': '',
        "'": '',
        '<': '',
        '>': '',
        '|': '',
        '+': '',
        '=': '',
        '&': '',
        '@': '',
        '#': '',
        '$': '',
        '%': '',
        '^': '',
        '*': '',
        '(': '',
        ')': '',
        '[': '',
        ']': '',
        '{': '',
        '}': '',
        '~': '',
        '`': '',
        '\\': '',
        '/': '',
      };
    
      return e
        .toString()
        .toLowerCase()
        .replace(/[^\w\s]/g, (char) => characters[char] || '')
        .replace(/\s+/g, '-')        
        .replace(/-+/g, '-')
        .replace(/^-+/, '')          
        .replace(/-+$/, '');
}

export default slug